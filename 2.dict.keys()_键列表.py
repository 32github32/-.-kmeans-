my_dict = {"name": "Alice", "age": 25, "city": "New York"}

# 使用 keys() 方法获取字典的键
keys = my_dict.keys()

print("字典的键:", keys)
#字典的键: dict_keys(['name', 'age', 'city'])

#————————————————————————————————————————————————————————————

key_list = list(my_dict.keys())

print("转换为列表:", key_list)


#转换为列表: ['name', 'age', 'city']
